// plural helpers
function simplePlural(singular: string, plural: string) {
  return (count: number) => `${count} ${count === 1 ? singular : plural}`;
}

function noPlural(word: string) {
  return (count: number) => `${count} ${word}`;
}

function slavicPlural(one: string, few: string, many: string) {
  return (count: number) => {
    const mod10 = count % 10;
    const mod100 = count % 100;

    if (mod10 === 1 && mod100 !== 11) {
      return `${count} ${one}`;
    }

    if (mod10 >= 2 && mod10 <= 4 && (mod100 < 10 || mod100 >= 20)) {
      return `${count} ${few}`;
    }

    return `${count} ${many}`;
  };
}

